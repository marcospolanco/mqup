import Foundation

/// Generates `docs/resume-bullet.md` strictly from measured eval output.
public enum ResumeBulletGenerator {
    public struct Input: Sendable {
        public let tunedAlpha: Double
        public let nDCGDelta: Double
        public let recallAt10: Double
        public let hybridP95LatencyMs: Double

        public init(tunedAlpha: Double, nDCGDelta: Double, recallAt10: Double, hybridP95LatencyMs: Double) {
            self.tunedAlpha = tunedAlpha
            self.nDCGDelta = nDCGDelta
            self.recallAt10 = recallAt10
            self.hybridP95LatencyMs = hybridP95LatencyMs
        }
    }

    public static func render(_ input: Input) -> String {
        let alpha = formatAlpha(input.tunedAlpha)
        let ndcgSigned = formatDeltaSigned(input.nDCGDelta)
        let recall = formatRecall(input.recallAt10)
        let latency = formatLatencyMs(input.hybridP95LatencyMs)

        return """
        # Maps Query Understanding Prototype — resume bullet

        > **Maps Query Understanding Prototype** (iOS, Swift). Built a hybrid lexical + semantic POI retrieval system over a 2,000-place synthetic SF/Cupertino dataset. Combined BM25 (SQLite FTS5) with deterministic hash embeddings (384-dim stand-in; Core ML MiniLM conversion staged per `models/README.md`); tuned blend weight α=\(alpha) on a 120-query dev split; evaluated on 150 constraint-labeled queries (token-overlap gold relevance, frozen before tuning) including a 30-query blind hold-out. Measured **nDCG@5 delta \(ndcgSigned)** and **Recall@10 \(recall)** against honest labels at **\(latency) p95** on macOS release harness (re-measure on iPhone 14+ before citing device latency). Integrated with App Intents (`SpatialVenueEntity` + `SearchPlacesIntent`, Spotlight entity matching) and indexes search results to Spotlight on launch and after each query. Authored a documented failure-and-fix series for representative ranking errors.

        Numbers sourced from `metrics.json` only. Regenerated by `MQUPEval`.
        """
    }

    public static func write(_ input: Input, to url: URL) throws {
        try render(input).write(to: url, atomically: true, encoding: .utf8)
    }

    static func formatAlpha(_ value: Double) -> String {
        String(format: "%.2f", value)
    }

    static func formatDelta(_ value: Double) -> String {
        String(format: "%.2f", value)
    }

    static func formatDeltaSigned(_ value: Double) -> String {
        String(format: "%+.2f", value)
    }

    static func formatRecall(_ value: Double) -> String {
        String(format: "%.2f", value)
    }

    static func formatLatencyMs(_ value: Double) -> String {
        let rounded = Int(value.rounded())
        return "\(rounded)ms"
    }
}
